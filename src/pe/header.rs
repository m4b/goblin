/// DOS header present in all PE binaries
#[repr(C, packed)]
#[derive(Debug, PartialEq, Copy, Clone, Default)]
pub struct DosHeader {
    /// Magic number: 5a4d
    pub signature: u16,
    /// Pointer to PE header, always at offset 0x3c
    pub pe_pointer: u32,
}

pub const DOS_MAGIC: u16 = 0x5a4d;
pub const DOS_HEADER_SIZE: usize = 0x40;
pub const PE_POINTER_OFFSET: u32 = 0x3c;

#[derive(Debug, Copy, Clone)]
struct DosHeaderCtx<'a> {
    pub sections: &'a [section_table::SectionTable],
}

impl<'a, 'b> scroll::ctx::TryFromCtx<'a, DosHeaderCtx<'b>> for DosHeader {
    type Error = error::Error;
    type Size = usize;
    #[inline]
    fn try_from_ctx(bytes: &'a [u8], DosHeaderCtx { sections }: DosHeaderCtx<'b>) -> Result<(Self, Self::Size), Self::Error> {
        let ptr: usize = 0;
        let signature = parse_field_by_name!(bytes, sections, ptr, DosHeader, signature, "cannot parse DOS signature (offset {:#x})");
        let pe_pointer = parse_field_by_offset!(bytes, sections, ptr, PE_POINTER_OFFSET as usize, "cannot parse PE header pointer (offset {:#x})");

        Ok((DosHeader { signature: signature, pe_pointer: pe_pointer }, DOS_HEADER_SIZE))
    }
}

impl DosHeader {
    pub fn parse(bytes: &[u8], sections: &[section_table::SectionTable]) -> error::Result<Self>  {
        bytes.pread_with(0, DosHeaderCtx { sections })
    }
}

pub const SIZEOF_COFF_HEADER: usize = 24;
/// PE\0\0, little endian
pub const COFF_MAGIC: u32 = 0x00004550;
pub const COFF_MACHINE_X86: u16 = 0x14c;
pub const COFF_MACHINE_X86_64: u16 = 0x8664;

implement_ctx_cstruct! {
    struct CoffHeader {
        signature: u32,
        machine: u16,
        number_of_sections: u16,
        time_date_stamp: u32,
        pointer_to_symbol_table: u32,
        number_of_symbol_table: u32,
        size_of_optional_header: u16,
        characteristics: u16,
    }
}

#[derive(Debug, PartialEq, Copy, Clone, Default)]
pub struct Header {
    pub dos_header: DosHeader,
    pub coff_header: CoffHeader,
    pub optional_header: Option<optional_header::OptionalHeader>,
}

impl Header {
    pub fn parse(bytes: &[u8], sections: &[section_table::SectionTable]) -> error::Result<Self> {
        let dos_header = DosHeader::parse(&bytes, sections)?;
        let offset = &mut (dos_header.pe_pointer as usize);
        let coff_header = CoffHeader::parse(&bytes, offset, sections)?;
        let optional_header = 
            if coff_header.size_of_optional_header > 0 {
                Some(bytes.pread_with::<optional_header::OptionalHeader>(0, CStructCtx { ptr: *offset as u32, sections })?)
            } else { None };

        Ok(Header { dos_header, coff_header, optional_header })
    }
}

